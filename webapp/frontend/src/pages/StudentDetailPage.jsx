import { useEffect, useState } from "react";
import { useParams, Link } from "react-router-dom";
import * as api from "../api/client";
import RiskBadge from "../components/RiskBadge";
import Card from "../components/Card";
import { LoadingState, ErrorBanner } from "./OverviewPage";

const PROFILE_FIELDS = [
  ["attendance_pct", "Attendance (%)"],
  ["internal_marks_avg", "Internal Marks (avg)"],
  ["prev_semester_pct", "Previous Semester (%)"],
  ["cgpa", "CGPA"],
  ["backlogs", "Backlogs"],
  ["assignment_submission_rate", "Assignment Submission (%)"],
  ["lms_login_freq_per_week", "LMS Logins / week"],
  ["library_visits_per_month", "Library Visits / month"],
  ["family_income_monthly", "Family Income (₹/month)"],
  ["first_generation_learner", "First-Gen Learner"],
  ["distance_from_college_km", "Distance from College (km)"],
  ["scholarship_status", "Scholarship"],
  ["fee_due", "Fee Due"],
  ["fee_delay_days", "Fee Delay (days)"],
  ["gender", "Gender"],
  ["category", "Category"],
  ["hostel_or_dayscholar", "Hostel / Day Scholar"],
];

const BOOLEAN_FIELDS = new Set([
  "first_generation_learner", "scholarship_status", "fee_due",
]);

export default function StudentDetailPage() {
  const { studentId } = useParams();
  const [student, setStudent] = useState(null);
  const [recommendations, setRecommendations] = useState(null);
  const [error, setError] = useState("");

  useEffect(() => {
    setStudent(null);
    setRecommendations(null);
    Promise.all([
      api.getStudent(studentId),
      api.getStudentRecommendations(studentId),
    ])
      .then(([s, r]) => {
        setStudent(s);
        setRecommendations(r);
      })
      .catch((e) => setError(e.message));
  }, [studentId]);

  if (error) return <ErrorBanner message={error} />;
  if (!student) return <LoadingState />;

  return (
    <div>
      <Link to="/students" style={styles.backLink}>← Back to Risk Dashboard</Link>

      <div style={styles.header}>
        <div>
          <div style={styles.eyebrow}>Student Record</div>
          <h1 style={{ fontSize: 24 }}>{student.student_id}</h1>
        </div>
        <div style={styles.scoreBlock}>
          <div style={styles.scoreValue}>{student.dropout_probability}%</div>
          <RiskBadge tier={student.risk_tier} />
        </div>
      </div>

      <div style={styles.grid}>
        <Card title="Student Profile" subtitle="Raw data used by the prediction model">
          <table>
            <tbody>
              {PROFILE_FIELDS.map(([key, label]) => (
                <tr key={key}>
                  <td style={styles.profileLabel}>{label}</td>
                  <td style={styles.profileValue}>
                    {BOOLEAN_FIELDS.has(key)
                      ? (student[key] ? "Yes" : "No")
                      : student[key]}
                  </td>
                </tr>
              ))}
            </tbody>
          </table>
        </Card>

        <Card title="Personalized Counseling Plan" subtitle="Generated by the rule-based recommendation engine">
          {recommendations && recommendations.length > 0 ? (
            <div style={styles.recList}>
              {recommendations.map((r, i) => (
                <div key={i} style={styles.recItem}>
                  <div style={styles.recIssue}>{r.issue}</div>
                  <div style={styles.recAction}>{r.recommended_action}</div>
                </div>
              ))}
            </div>
          ) : (
            <p style={{ color: "var(--color-slate-500)", fontSize: 14 }}>
              No specific risk factors flagged for this student.
            </p>
          )}
        </Card>
      </div>
    </div>
  );
}

const styles = {
  backLink: {
    fontSize: 13,
    color: "var(--color-teal-700)",
    fontWeight: 600,
    display: "inline-block",
    marginBottom: "var(--space-5)",
  },
  header: {
    display: "flex",
    justifyContent: "space-between",
    alignItems: "flex-end",
    marginBottom: "var(--space-6)",
  },
  eyebrow: {
    fontSize: 12,
    fontWeight: 600,
    letterSpacing: "0.06em",
    textTransform: "uppercase",
    color: "var(--color-slate-500)",
    marginBottom: 4,
  },
  scoreBlock: {
    textAlign: "right",
    display: "flex",
    flexDirection: "column",
    alignItems: "flex-end",
    gap: 8,
  },
  scoreValue: {
    fontFamily: "var(--font-display)",
    fontSize: 28,
    fontWeight: 700,
  },
  grid: {
    display: "grid",
    gridTemplateColumns: "1fr 1fr",
    gap: "var(--space-5)",
    alignItems: "start",
  },
  profileLabel: {
    padding: "8px 0",
    fontSize: 13,
    color: "var(--color-slate-500)",
    borderBottom: "1px solid var(--color-slate-100)",
  },
  profileValue: {
    padding: "8px 0",
    fontSize: 13,
    fontWeight: 600,
    textAlign: "right",
    borderBottom: "1px solid var(--color-slate-100)",
  },
  recList: {
    display: "flex",
    flexDirection: "column",
    gap: "var(--space-3)",
  },
  recItem: {
    background: "var(--color-risk-medium-bg)",
    borderRadius: "var(--radius-sm)",
    padding: "var(--space-4)",
  },
  recIssue: {
    fontSize: 13,
    fontWeight: 700,
    color: "var(--color-risk-medium)",
    marginBottom: 4,
  },
  recAction: {
    fontSize: 13,
    color: "var(--color-navy-800)",
    lineHeight: 1.5,
  },
};
